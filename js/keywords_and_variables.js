//variable : it is a container it is used to store anytype of data 
// keywords : it is a predefined words
// note :- all keywords  written in lowercase

// declaration of variable
// var : here all the case are possible like declaration ,initialization, reinitialization,redeclaration. var keywords is a es1 version keyword
var a; //declaration
a = 100 //initialization
console.log(a);     // output: undefined

a = "hello" //reinitialization
console.log(a);     // output: undefined

var a;          //redeclaration
console.log(a);     // output: undefined

a = true;           //reinitialization
console.log(a);     // output: true


// let : here all the case are possible like declaration ,initialization, reinitialization,redeclaration is not possible in let keyword let is a es6 version keyword

let b;  //declaration
b = 200 //initialization
console.log(b);     //output: 200

b = 400 //reinitialization
console.log(b);     //output: 400


